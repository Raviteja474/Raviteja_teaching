def addition(a=0,b=0):
    print(f"For addition, Input values are :{a} and {b}")
    return a+b
def substract(a=0,b=0):
    print(f"For substract,Input values are :{a} and {b}")
    return a-b
def multiply(a=0,b=0):
    print(f"For multiply,Input values are :{a} and {b}")
    return a*b
def division(a=0,b=1):
    print(f"For division,Input values are :{a} and {b}")
    return a/b
